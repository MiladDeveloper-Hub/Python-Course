# loops in python

# for item in collection
#   pass

# collection => (list of numbers) , (list of characters in a string) , (range(1, 10)) , Etc...


# list of numbers
listOfNumbers = [34, 23, 23, 53, 35, 46]
for number in listOfNumbers:
    print(number)

print("---------")

# list of characters in a string
myName = "milad"
for char in myName:
    print(char)

print("---------")

# range 1 to 100
rangeOfNumbers = range(1, 101)
for number in rangeOfNumbers:
    print(number)
